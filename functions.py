def open_site(p, url: str = "https://www.eduland.ir/"):
    """
    باز کردن سایت با Playwright.
    در صورت خطا: با detect_and_report_bug اسکرین‌شات و گزارش می‌گیرد.
    """
    browser = None
    context = None
    page = None
    try:
        browser = p.chromium.launch(headless=False)
        context = browser.new_context()
        page = context.new_page()
        page.goto(url)
        page.wait_for_timeout(2000)
        print(f"✅ Opened site: {url}")

        # چک کنیم آیا صفحه درست لود شده (مثلاً لوگوی سایت دیده بشه یا متن خاصی)
        bug = detect_and_report_bug(
            page,
            chapter="system",
            skill="open_site",
            stage="after_open",
            require_submit_visible=False  # اینجا فقط خطاهای عمومی رو چک می‌کنیم
        )
        if bug:
            raise Exception(f"Site open detected bug: {bug}")

        return browser, context, page

    except Exception as e:
        print(f"❌ Failed to open site: {str(e)}")
        if page:
            detect_and_report_bug(
                page,
                chapter="system",
                skill="open_site",
                stage="exception"
            )
        if browser:
            browser.close()
        raise


